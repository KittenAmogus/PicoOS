.syntax unified
.cpu cortex-m0plus
.thumb

.section .text
.global _stage2_boot
.thumb_func

_stage2_boot:
    push {lr}

    // Базовый адрес контроллера SSI (0x18000000)
    ldr r3, =0x18000000

    // 1. Отключаем SSI для настройки (пишем 0 в SSIENR, смещение 0x08)
    movs r1, #0
    str r1, [r3, #0x08]

    // 2. Устанавливаем делитель частоты SPI = 4 (в BAUDR, смещение 0x14)
    movs r1, #4
    str r1, [r3, #0x14]

    // 3. Настраиваем CTRLR0 (смещение 0x00)
    // Стандартный 1-битный SPI, 32 бита на фрейм, EEPROM Read
    ldr r1, =0x000f0300
    str r1, [r3, #0x00]

    // 4. Настраиваем SPI_CTRLR0 (смещение 0xF4)
    // Обходим ограничение Thumb: прибавляем 0xF4 к базовому r3 в отдельный регистр r2
    movs r2, #0xF4
    adds r2, r2, r3      // Теперь в r2 лежит точный адрес 0x180000F4
    
    ldr r1, =0x00030610  // Команда чтения 0x03, 24 бита адреса
    str r1, [r2, #0x00]  // Пишем без смещения прямо по адресу из r2

    // 5. Включаем SSI обратно (пишем 1 в SSIENR, смещение 0x08)
    movs r1, #1
    str r1, [r3, #0x08]

    // Выход из boot2: перенаправляем процессор на векторную таблицу ОС (адрес 0x10000100)
    ldr r0, =0x10000100
    ldr r1, [r0, #4]    // Берем адрес Reset_Handler из таблицы векторов
    blx r1              // Прыгаем туда!

.align 4
